//10. Check if a Number is Positive and Even

public class A10 {
    public static void main(String[] args) {
        int num = 12;
        if (num > 0 && num % 2 == 0) {
            System.out.println(num + " is positive and even.");
        } else {
            System.out.println(num + " is not positive and even.");
        }
    }
}